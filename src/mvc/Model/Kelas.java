public class Kelas {
    private Integer id;
    private String kelasId;
    private String namaKelas;
    private String nipWaliKelas;
    private Integer tahunAjaran;
    private Integer capacity;
   

    /**
     * @return the id
     */
    public Integer getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(Integer id) {
        this.id = id;
    }

    /**
     * @return the kelasId
     */
    public String getKelasId() {
        return kelasId;
    }

    /**
     * @param kelasId the kelasId to set
     */
    public void setKelasId(String kelasId) {
        this.kelasId = kelasId;
    }

    /**
     * @return the namaKelas
     */
    public String getNamaKelas() {
        return namaKelas;
    }

    /**
     * @param namaKelas the namaKelas to set
     */
    public void setNamaKelas(String namaKelas) {
        this.namaKelas = namaKelas;
    }

    /**
     * @return the nipWaliKelas
     */
    public String getNipWaliKelas() {
        return nipWaliKelas;
    }

    /**
     * @param nipWaliKelas the nipWaliKelas to set
     */
    public void setNipWaliKelas(String nipWaliKelas) {
        this.nipWaliKelas = nipWaliKelas;
    }

    /**
     * @return the tahunAjaran
     */
    public Integer getTahunAjaran() {
        return tahunAjaran;
    }

    /**
     * @param tahunAjaran the tahunAjaran to set
     */
    public void setTahunAjaran(Integer tahunAjaran) {
        this.tahunAjaran = tahunAjaran;
    }

    /**
     * @return the capacity
     */
    public Integer getCapacity() {
        return capacity;
    }

    /**
     * @param capacity the capacity to set
     */
    public void setCapacity(Integer capacity) {
        this.capacity = capacity;
    }
    
}
